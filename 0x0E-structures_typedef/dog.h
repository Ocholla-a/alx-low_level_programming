#ifndef DOG_H
#define DOG_H

/**
 * struct dog - defining structure of a dog
 * @name: first property which is the name of the dog
 * @owner: second property which is the name of the owner of the dog
 * @age: third property which is the age of the dog
 */

struct dog
{
	char *name;
	char *owner;
	float age;
};

#endif
